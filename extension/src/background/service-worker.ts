chrome.runtime.onMessage.addListener((msg: RuntimeMessage, _sender, sendResponse) => {
  (async () => {
    switch (msg.type) {
      case 'GET_STATE':
        sendResponse(getState());
        break;

      case 'UPDATE_SETTINGS':
        settings = { ...settings, ...msg.settings };
        await saveSettings();
        broadcast({ type: 'STATE_UPDATE', state: getState() });
        sendResponse({ ok: true });
        break;

      case 'TOGGLE_REVIEW_MODE': {
        settings.reviewModeEnabled = msg.enabled;
        await saveSettings();
        // Forward to active tab's content script
        const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });
        if (tab?.id) {
          chrome.tabs.sendMessage(tab.id, { type: 'TOGGLE_REVIEW_MODE', enabled: msg.enabled });
        }
        broadcast({ type: 'STATE_UPDATE', state: getState() });
        sendResponse({ ok: true });
        break;
      }

      case 'PAGE_CONTEXT_UPDATE':
        pageContext = msg.context;
        break;

      case 'ELEMENT_SELECTED':
        addTimelineItem({
          id: crypto.randomUUID(),
          action: 'element_selected',
          summary: `Selected <${msg.element.tag}> "${msg.element.text.slice(0, 60)}"`,
          url: msg.element.url,
          timestamp: new Date().toISOString(),
        });
        break;

      case 'ADD_TIMELINE_ITEM':
        addTimelineItem(msg.item);
        break;

      case 'SEND_REVIEW_REQUEST': {
        const req = msg.request;
        addTimelineItem({
          id: crypto.randomUUID(),
          action: 'request_sent',
          summary: `Sent review request: "${req.userMessage.slice(0, 80)}"`,
          url: req.pageContext.url,
          timestamp: new Date().toISOString(),
        });

        // Immediately ack; stream progress updates via broadcast
        broadcast({
          type: 'REVIEW_RESPONSE',
          response: { requestId: req.requestId, stage: 'understanding' },
        });

        try {
          const response = await sendToBackend(req);
          broadcast({ type: 'REVIEW_RESPONSE', response });
          addTimelineItem({
            id: crypto.randomUUID(),
            action: 'backend_response',
            summary: `Backend responded: ${response.summary ?? response.stage}`,
            url: req.pageContext.url,
            timestamp: new Date().toISOString(),
          });
          sendResponse({ ok: true, response });
        } catch (err) {
          const error = err instanceof Error ? err.message : String(err);
          const errResponse: ReviewResponse = {
            requestId: req.requestId,
            stage: 'error',
            error,
          };
          broadcast({ type: 'REVIEW_RESPONSE', response: errResponse });
          sendResponse({ ok: false, error });
        }
        break;
      }

      case 'APPROVE_CHANGES': {
        addTimelineItem({
          id: crypto.randomUUID(),
          action: 'approved',
          summary: `Approved changes for request ${msg.requestId}`,
          url: pageContext?.url ?? '',
          timestamp: new Date().toISOString(),
        });

        // Tell backend to apply
        try {
          await fetch(`${settings.backendUrl}/apply`, {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
              ...(settings.backendAuthToken ? { Authorization: `Bearer ${settings.backendAuthToken}` } : {}),
            },
            body: JSON.stringify({ requestId: msg.requestId }),
          });
        } catch (_) { /* non-fatal */ }

        sendResponse({ ok: true });
        break;
      }

      case 'REJECT_CHANGES':
        addTimelineItem({
          id: crypto.randomUUID(),
          action: 'rejected',
          summary: `Rejected changes for request ${msg.requestId}`,
          url: pageContext?.url ?? '',
          timestamp: new Date().toISOString(),
        });
        sendResponse({ ok: true });
        break;

      // Content script requests a screenshot via background (only background can capture tabs)
      default:
        if ((msg as any).type === 'CAPTURE_SCREENSHOT') {
          const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });
          if (tab?.id) {
            try {
              const dataUrl = await chrome.tabs.captureVisibleTab(tab.windowId!, { format: 'png' });
              sendResponse({ dataUrl });
            } catch {
              sendResponse({ dataUrl: null });
            }
          } else {
            sendResponse({ dataUrl: null });
          }
        }
        break;
    }
  })();
  return true; // keep channel open for async
});

